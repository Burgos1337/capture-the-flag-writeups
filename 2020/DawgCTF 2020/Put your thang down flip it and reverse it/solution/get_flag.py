s = [0x41, 0xF5, 0x51, 0xD1, 0x4D, 0x61, 0xD5, 0xE9, 0x69, 0x89, 
     0x19, 0xDD, 0x09, 0x11, 0x89, 0xCB, 0x9D, 0xC9, 0x69, 0xF1, 
     0x6D, 0xD1, 0x7D, 0x89, 0xD9, 0xB5, 0x59, 0x91, 0x59, 0xB1, 
     0x31, 0x59, 0x6D, 0xD1, 0x8B, 0x21, 0x9D, 0xD5, 0x3D, 0x19, 
     0x11, 0x79, 0xDD]

flag = ''
for i in range(len(s)):
    for h in range(-256, 0, 1):
        v = 0
        for j in range(0, 8):
            if h & (1 << j) != 0:
                v = v | (1 << (8 - j - 1))
        if(v == s[i]):
            flag += chr(~h)
               
print(flag[::-1])
